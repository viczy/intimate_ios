import UIKit

extension UIColor {

    class func skyBlue () -> UIColor {
        return UIColor(red: 32.0/255.0, green: 184.0/255.0, blue: 232.0/255.0, alpha: 1)
    }

    class func earthRed () -> UIColor {
        return UIColor(red: 255.0/255.0, green: 102.0/255.0, blue: 153.0/255.0, alpha: 1)
    }

    class func forestBlack () -> UIColor {
        return UIColor(red: 0.0/255.0, green: 0.0/255.0, blue: 51.0/255.0, alpha: 1)
    }

}
